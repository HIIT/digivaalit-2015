import requests

from bs4 import BeautifulSoup
import bs4

def nouda( url , out ):

	r = requests.get( url )
	r.encoding = 'UTF-8'
	soup = BeautifulSoup( r.text )

	teksti = soup.find_all( class_='Teksti' )

	for string in teksti[0].stripped_strings:
	        out.write( string.encode('utf8') + ' ' )

